from PyQt6.QtCore import QByteArray, QSize, Qt
from PyQt6.QtGui import QIcon, QPixmap, QPainter, QColor
from PyQt6.QtSvg import QSvgRenderer


def _svg_to_icon(svg_str: str, size: int = 20, color: str = "#444") -> QIcon:
    """Convert an SVG string to a QIcon, replacing stroke color."""
    svg_str = svg_str.replace('stroke="currentColor"', f'stroke="{color}"')
    data = QByteArray(svg_str.encode("utf-8"))
    renderer = QSvgRenderer(data)
    pixmap = QPixmap(size, size)
    pixmap.fill(Qt.GlobalColor.transparent)
    painter = QPainter(pixmap)
    renderer.render(painter)
    painter.end()
    return QIcon(pixmap)


def icon(name: str, size: int = 20, color: str = "#444") -> QIcon:
    """Get a named icon. Returns empty QIcon if name not found."""
    svg = _ICONS.get(name)
    if not svg:
        return QIcon()
    return _svg_to_icon(svg, size, color)


